export function HeightOfGlyph(glyph: Glyph)
{
	const code = glyph.codePointAt(0)

	if (code == undefined)
	{
		console.error(`Invalid glyph: ${glyph}`)
		return 1
	}

	if (! (code in UnicodeHeight))
	{
		console.error(`Missing height for character: U+${code.toString(16).toUpperCase().padStart(4, '0')} (${glyph})`)
		return 1
	}

	return UnicodeHeight[code] / 1000
}

const LineHeightKey = "egyptian-line-height"

export function SetLineHeight(h: number)
{
	setContext(LineHeightKey, h)
}

export function GetLineHeight()
{
	return getContext<number>(LineHeightKey)
}
