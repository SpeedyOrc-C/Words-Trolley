export function WidthOfGlyph(glyph: string)
{
   if (glyph == "" || glyph == "")
      return WidthOfGlyph("")

   if (glyph == "" || glyph == "")
      return WidthOfGlyph("")

	if (glyph == "")
		return WidthOfGlyph("")

	if (glyph == "")
		return WidthOfGlyph("")

   const code = glyph.codePointAt(0)

   if (code == undefined)
   {
      console.error(`Invalid glyph: ${glyph}`)
      return 1
   }

   if (! (code in UnicodeWidth))
   {
      console.error(`Missing height for character: U+${code.toString(16).toUpperCase().padStart(4, '0')} (${glyph})`)
      return 1
   }

   return UnicodeWidth[code] / 1000
}
